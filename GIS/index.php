<div class="textblock"><p><a class="anchor" id="md_GIS_README"></a> This group of tools processes eFRI and other data to create fuel, slope, aspect and elevation rasters.</p>
<h1><a class="anchor" id="autotoc_md0"></a>
Introduction</h1>
<h1><a class="anchor" id="autotoc_md1"></a>
Setup</h1>
<h2><a class="anchor" id="autotoc_md2"></a>
Step 1: Run the FireSTARR setup</h2>
<p>This should properly set up a python environment</p>
<h2><a class="anchor" id="autotoc_md3"></a>
Step 2: Run grids/collect.py</h2>
<p>This should get some base data that is required for generating the grids</p>
<h2><a class="anchor" id="autotoc_md4"></a>
Step 3: Run earthenv.py</h2>
<p>This should make the DEM for the area defined in the settings file</p>
<h2><a class="anchor" id="autotoc_md5"></a>
Step 4: Run canvec/canvec.py</h2>
<p>This should collect the canvec data that gets used for making more accurate water in the grids. To minimize amount of downloaded data, run <code>python <a class="el" href="canvec_8py.php">canvec.py</a> extract Hydro</code> to just get water data.</p>
<h2><a class="anchor" id="autotoc_md6"></a>
Step 5: Run grids/make_grids.py</h2>
<p>This should generate the rasters that are required for FireSTARR to work</p>
<h2><a class="anchor" id="autotoc_md7"></a>
Step 6: Place the grids in ../100m/default</h2>
<p>This will make the grids the default grids used for the FireSTARR simulations. Otherwise, place them in ../100m/{YEAR} to have them be used for simulations occurring during a certain year. </p>
